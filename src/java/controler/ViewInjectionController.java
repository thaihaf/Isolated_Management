    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        String username = request.getParameter("username");
        InjectionDBContext db = new InjectionDBContext();
        ArrayList<InjectionReport> lists = db.getInjectionResultByID(username);
        request.setAttribute("lists", lists);
        request.setAttribute("patient", username);
        request.getRequestDispatcher("../view/viewInjection.jsp").forward(request, response);
    }

    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        processRequest(request, response);
    }

